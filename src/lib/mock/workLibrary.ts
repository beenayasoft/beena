// Fonctions utilitaires pour récupérer les données
export const getWorkCategories = (parentId?: string) => {
  if (parentId) {
    return mockWorkSubCategories.filter(cat => cat.parentId === parentId);
  }
  return mockWorkCategories;
};

export const getWorksByCategoryId = (categoryId: string) => {
  return mockWorks.filter(work => work.categoryId === categoryId);
};

export const getWorkById = (id: string) => {
  return mockWorks.find(work => work.id === id);
};

export const getMaterialById = (id: string) => {
  return mockMaterials.find(material => material.id === id);
};

export const getLaborById = (id: string) => {
  return mockLabor.find(labor => labor.id === id);
}; 